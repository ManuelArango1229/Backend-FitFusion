


/**
 * Enum representing the different roles a user can have within the system.
 * 
 * @enum {string}
 * @property {string} USER - Represents a standard user role.
 * @property {string} TRAINER - Represents a trainer role.
 * @property {string} ADMIN - Represents an administrator role.
 */
export enum UserRole {
    USER = "USER",
    TRAINER = "TRAINER",
    ADMIN = "ADMIN",
  }